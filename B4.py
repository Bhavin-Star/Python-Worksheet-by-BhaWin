a = int(input('Enter number from 1 to 7 to find the corresponding day: '))

if(a==1):
    print('Sunday')

elif(a==2):
    print('Monday')

elif(a==3):
    print('Tuesday')

elif(a==4):
    print('Wednesday')

elif(a==5):
    print('Thursday')

elif(a==6):
    print('Friday')

elif(a==7):
    print('Saturday')

else:
    print('Enter a whole number number from 1 to 7')
